var primeiroNumero = parseFloat(prompt("Digite o primeiro número: "));
var segundoNumero = parseFloat(prompt("Digite o segundo número: "));

alert("Soma: " + (primeiroNumero + segundoNumero));
alert("Subtração: " + (primeiroNumero - segundoNumero));
alert("Multiplicação: " + (primeiroNumero * segundoNumero));
alert("Divisão: " + (primeiroNumero / segundoNumero));
alert("Resto: " + (primeiroNumero % segundoNumero));